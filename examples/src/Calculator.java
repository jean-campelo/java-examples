public class Calculator {
    /**
     * Method for adding two integer values
     * 
     * @params num1 first value
     * 
     * @params num2 second value
     * 
     * @return int result of the sum between num1 and num2
     */

    public int sum(int num1, int num2) {
        return num1 + num2;
    }
}
